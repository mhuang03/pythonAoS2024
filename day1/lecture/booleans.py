# a boolean is a data type that can only have one of two values: True or False
# booleans are used in conditional statements and loops to control the flow of a program

# the following are the basic comparison operators, which return a boolean value (True or False):
print(3 == 4) # False
print(3 != 4) # True

print(3 > 4) # False
print(3 < 4) # True

print(3 >= 4) # False
print(3 <= 4) # True

# the following are the basic boolean operators in python:
print(True and False) # False
print(True or False)  # True
print(not True)       # False
print(not False)      # True